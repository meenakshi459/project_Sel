public class ArrayDupAlter {

	public static void main(String[] args) {
		
	
	
       int[] num = {2,5,7,7,5,9,2,3};		
     
       System.out.println("Duplicate numbers are as below: ");
       
       for (int i=0; i<num.length;i++) {
    	         
    	   
    	   if (num[i]==num[i++]);{
    	   
    	   System.out.println(num[i]);
			
		}
		
		}
	}
	
}


	


